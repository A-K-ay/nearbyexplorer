import 'package:nearbyexplorer/core/constants.dart';

import '../widgets/showToast.dart';
import 'package:geolocator/geolocator.dart';
import 'package:geocoding/geocoding.dart';

class LocationServices {
  /// get address from coordinates provided in the input
  static Future<String> searchAdressFromCoordinates(Position position) async {
    List<Placemark> placemarks =
        await placemarkFromCoordinates(32.244457492247015, 77.18599319458009);
    print(placemarks[0].name + placemarks[0].street + placemarks[0].locality);
    return "${placemarks[0].name}, ${placemarks[0].street}, ${placemarks[0].locality}";
  }

  /// get loaction coordinates from the address provided in the input
  static Future<Location> searchCoordinateFromAddress(String address) async {
    List<Location> locations = await locationFromAddress(address);
    print(locations[0]);
    return locations[0];
  }

  /// Determine the current position of the device.
  /// When the location services are not enabled or permissions
  /// are denied the `Future` will return an error.
  static Future determinePosition(context) async {
    bool serviceEnabled;
    LocationPermission permission;

    // Test if location services are enabled.
    serviceEnabled = await Geolocator.isLocationServiceEnabled();
    if (!serviceEnabled) {
      // Location services are not enabled don't continue
      ShowToast(context)
          .showToastNotification("Please Enable Location Services");
      // accessing the position and request users of the
      // App to enable the location services.
      return Future.error('Location services are disabled.');
    }

    permission = await Geolocator.checkPermission();
    if (permission == LocationPermission.denied) {
      permission = await Geolocator.requestPermission();
      if (permission == LocationPermission.deniedForever) {
        print(
            "LocationPermission.deniedForever ?????????????????????????????????????????????????????????????????????????????????????????????????????????");
        Constants.isLocationdenied = true;
        print(
            "???????????????????????????????????????????????????????????????????????????????????????????????????????????????? ${Constants.isLocationdenied}");
        // Permissions are denied forever, handle appropriately.
        return false;
      }

      if (permission == LocationPermission.denied) {
        // Permissions are denied, next time you could try
        print(
            "LocationPermission.denied ?????????????????????????????????????????????????????????????????????????????????????????????????????????");
        Constants.isLocationdenied = true;
        print(
            "???????????????????????????????????????????????????????????????????????????????????????????????????????????????? ${Constants.isLocationdenied}");
        // requesting permissions again (this is also where
        // Android's shouldShowRequestPermissionRationale
        // returned true. According to Android guidelines
        // your App should show an explanatory UI now.
        return false;
      }
    }
    if (permission == LocationPermission.whileInUse) {
      Constants.isLocationdenied = false;
    }
    if (permission == LocationPermission.always) {
      Constants.isLocationdenied = false;
    }

    // When we reach here, permissions are granted and we can
    // continue accessing the position of the device.
    return true;
  }

  static Future<String> getAddressFromMyLocation(context) async {
    print("Trying to get position");
    Position position = await determinePosition(context);
    String address = await searchAdressFromCoordinates(position);
    return address;
  }
}
